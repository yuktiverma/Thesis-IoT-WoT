package beans;

public class Sensor {
	String sensorName;
	String sensorFields;
	public String getSensorName() {
		return sensorName;
	}
	public void setSensorName(String sensorName) {
		this.sensorName = sensorName;
	}
	public String getSensorFields() {
		return sensorFields;
	}
	public void setSensorFields(String sensorFields) {
		this.sensorFields = sensorFields;
	}
	

}
